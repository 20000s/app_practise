var initI18N = function () {
    console.log(LANGUAGE, 'LANGUAGE born')
    LANGUAGE = LANGUAGE ? LANGUAGE : 'zh_HK';
    var languageWrap = LANGUAGE;

    //根据版本来区分文案: 0普通版，1家校版, 2政务版, 3教培版
    var version;

    try {
        version = VERSION - 0 ? VERSION - 0 : 0;
    } catch (e) {
        version = 0;
    }


    switch (version) {
        case 0:
            language = i18n[LANGUAGE];
            break;
        // case 1:
        //     language = i18n_edu[LANGUAGE];
        //     break;
        case 2:
            language = i18n_gov[LANGUAGE];
            break;
        case 3:
            language = i18n_edutrain[LANGUAGE];
            break;
        default:
            language = i18n[LANGUAGE];
    }

    $('body').addClass(languageWrap);

    $('.i18n').each(function () {
        var key = $(this).attr('name');
        $(this).text(language[key]);
    });
};

initI18N();
